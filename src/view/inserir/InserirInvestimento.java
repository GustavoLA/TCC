    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        Investimento i = new Investimento();

        i.setDescricaoInvestimento(txDescricao.getText());
        i.setFormaPagamento(cbPagamento.getSelectedItem() + "");
//combo i.setPagamento(txFormaPagamento.getText());
        i.setNumeroParcelas(Integer.parseInt(txNumeroParcela.getText()));
        i.setValorParcela(Double.parseDouble(txValorParcela.getText()));
        i.setValorTotal(Double.parseDouble(txValorTotal.getText()));

        try {
            String data = txDtInvestimento.getText();
            i.setDtInvestimento(new SimpleDateFormat("dd/mm/yyyy").parse(data));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "FORMATO INVÁLIDO! UTILIZE: DD/MM/YYYY\n" + ex);
        }

        InvestimentoController ic = new InvestimentoController();

        if (!(txId.getText().equals("") | (txId.getText().equals(null)))) {
            i.setCodigo(Integer.parseInt(txId.getText()));
            ic.salvar(i);

            modelo.removeRow(linhaSelecionada);
            modelo.addRow(new Object[]{i.getCodigo(), i.getDescricaoInvestimento(), i.getDtInvestimento(), i.getFormaPagamento(), i.getNumeroParcelas(), i.getValorParcela(), i.getValorTotal()});
            dispose();
        } else {
            ic.salvar(i);
            dispose();
        }
    }//GEN-LAST:event_btSalvarActionPerformed


