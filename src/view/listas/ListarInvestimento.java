    private void criaJTable() {

        tabela = new JTable(modelo) {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        modelo.addColumn("ID");
        modelo.addColumn("DESCRIÇÃO");
        modelo.addColumn("DATA");

        preencherJTable();
    }

    private void preencherJTable() {

        InvestimentoController ic = new InvestimentoController();

        for (Investimento i : ic.listarInvestimento()) {
            modelo.addRow(new Object[]{i.getCodigo(), i.getDescricaoInvestimento(), i.getDtInvestimento(), i.getFormaPagamento(), i.getNumeroParcelas(), i.getValorParcela(), i.getValorTotal()});

        }

    }
